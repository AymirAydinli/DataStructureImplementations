
# Stack implementation using list

stack = []

# append function to push elements into stack

stack.append(1)
stack.append(2)
stack.append(3)
stack.append(4)

# Initial stack
print(stack)

# Biggest characteristics of stack is elemets being in LIFO (Last In First Out) order.
# I will use pop() funtion ti mimick LIFO

print('\nElemets popped from stack: ')
print(stack.pop())
print(stack.pop())

print('\nLast version of stack: ')
print(stack)





